let num1=21;
let num2=7;
let char='/';
switch(char){
    case '+':
      console.log(num1+num2);
      break;
    case '-':
      console.log(num1-num2);
      break;
    case '*':
        console.log(num1*num2);
        break;
    case '/':
        console.log(num1/num2);
        break;
    default:
        console.log("invalid input");
        break;
}